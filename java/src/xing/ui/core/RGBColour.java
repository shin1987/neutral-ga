package xing.ui.core;

/**
 * @author Xing Yu
 */
public class RGBColour
{
    public final int r, g, b;
    public RGBColour(final int r, final int g, final int b)
    {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public RGBColour()
    {
        this(0, 0, 0);
    }
} // class RGBColour
